import re

# Список "плохих" слов и их замен
BAD_WORDS = {
    # Русские слова
    'блядь': '***',
    'блять': '***',
    'хуй': '***',
    'пиздец': '***',
    'сука': '***',
    'ебать': '***',
    'жопа': '***',
    'мудак': '***',
    'придурок': '***',
    'козёл': '***',
    'засранец': '***',
    # Кыргызские слова
    'канчык': '***',
    'жалап': '***',
    'итин баласы': '***',
    'көкмээ': '***',
    'мырк': '***',
}

def filter_bad_words(text):
    """
    Фильтрует ненормативную лексику в тексте.
    """
    for bad_word, replacement in BAD_WORDS.items():
        # Используем re.IGNORECASE для игнорирования регистра
        pattern = re.compile(r'\b' + re.escape(bad_word) + r'\b', re.IGNORECASE)
        text = pattern.sub(replacement, text)
    return text